#!/usr/bin/python3
def add(a, b):
    """Addition function
    Args:
    a: first int
    b: second int

    Returns:
    The value of a + b
    """
    return (a + b)

def sub(a, b):
    """My subtraction function
    Args:
    a: first integer
    b: second it

    Returns:
    returns the value of a - b
    """
    return (a - b)

def div(a, b):
    """Division function
    Args:
    a: first num
    b: second num

    Returns:
    the value of a / b
    """
    return int(a / b)

def mul(a, b):
    """Multiplication function
    Args:
    a: first int
    b: second int

    Returns:
    the value of a * b
    """

    return (a * b)
